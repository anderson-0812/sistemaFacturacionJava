    private void btn_guardar_rol_jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardar_rol_jButton1ActionPerformed
        // TODO add your handling code here:
        Rol obj_rol = new Rol();
        // lenamos el nombre del rol con el campod ela interfaz 
        obj_rol.setNombre_rol(txt_nombreRol_jTextField1.getText());
        
        // usamos el servicio
        this.sr.fijarInstancia(obj_rol);
        
        if(this.sr.guardar()){
            JOptionPane.showMessageDialog(this, "Rol guardado exitosamente");
        }else{
            JOptionPane.showMessageDialog(this, "Rol no se ha guardado");
        }
        this.sr.nuevaInstancia();
        this.txt_nombreRol_jTextField1.setText("");
        cargarDatosTablaRoles();
    }//GEN-LAST:event_btn_guardar_rol_jButton1ActionPerformed

    private void btnEliminarRolActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarRolActionPerformed
        // TODO add your handling code here:
        int rol_seleccionado = this.tablaRoles_jTable1.getSelectedRow();
        //Rol objeto_eliminar = this.lista_roles.get(rol_seleccionado);
        //System.out.println(objeto_eliminar.getNombre_rol());
        int opcion = JOptionPane.showConfirmDialog(this, "Est√°s seguro que deseas eliminar");
        this.sr.fijarInstancia(this.lista_roles.get(rol_seleccionado));
        if (opcion == 0) {
            if (this.sr.eliminar(this.lista_roles.get(rol_seleccionado))) {
                JOptionPane.showMessageDialog(this, "OBJETO ELIMINADO");
                cargarDatosTablaRoles();
            } else {
                JOptionPane.showMessageDialog(this, "NO SE PUDO ELIMINAR");
            }
        } 
    }//GEN-LAST:event_btnEliminarRolActionPerformed


