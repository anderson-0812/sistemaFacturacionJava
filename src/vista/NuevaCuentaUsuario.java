    public void llenarComboRoles() {
        this.lista_roles = this.sr.listar();
        for (int i = 0; i < this.lista_roles.size(); i++) {
            this.cmbRolCuenta.addItem(this.lista_roles.get(i).getNombre_rol());
        }
        cmbRolCuenta.updateUI();
    }

