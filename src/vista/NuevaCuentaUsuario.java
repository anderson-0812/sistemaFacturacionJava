    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        this.obj_usuario.setApellido_usuario(txtApellidosUsuario.getText());
        this.obj_usuario.setCedula_usuario(txtCedulaUsuario.getText());
        this.obj_usuario.setEmail_usuario(txtEmailUsuario.getText());
        this.obj_usuario.setNombre_usuario(txtNombresUsuario.getText());
        this.obj_usuario.setTelefono_usuario(txtTelefonoUsuario.getText());
        

        this.obj_cuenta.setUsername(txtUsername.getText());
        this.obj_cuenta.setPassword(pswPasswordCuenta.getText());
        this.obj_cuenta.setEstado_usuario(chkEstadoCuenta.isSelected());
        this.obj_cuenta.setObjeto_usuario(this.obj_usuario);

        this.obj_rol = this.lista_roles.get(cmbRolCuenta.getSelectedIndex());

        this.obj_cuenta.setObjeto_rol(this.obj_rol);
        
        this.su.fijarInstancia(this.obj_usuario);
        this.sc.fijarInstancia(this.obj_cuenta);

        if (pswPasswordCuenta.getText().equals(pswPasswordCuenta2.getText())) {
            if (this.su.guardar() && this.sc.guardar()) {
                JOptionPane.showMessageDialog(this, "CUENTA GUARDADA CON ÉXITO");
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(this, "NO SE HA PODIDO CREAR LA CUENTA");
                this.obj_cuenta = new Cuenta();
                this.obj_usuario = new Usuario();
            }
        } else {
            JOptionPane.showMessageDialog(this, "LAS CONTRASEÑAS NO COINCIDEN");
        }


    }//GEN-LAST:event_jButton1ActionPerformed

    public void limpiarCampos(){
        txtApellidosUsuario.setText("");
        txtCedulaUsuario.setText("");
        txtEmailUsuario.setText("");
        txtNombresUsuario.setText("");
        txtTelefonoUsuario.setText("");
        txtUsername.setText("");
        pswPasswordCuenta.setText("");
        pswPasswordCuenta2.setText("");
        chkEstadoCuenta.setSelected(false);
        cmbRolCuenta.setSelectedIndex(0);
        this.sc.nuevaInstancia();
        this.su.nuevaInstancia();
        this.obj_cuenta = null;
        this.obj_usuario = null;
        this.obj_rol = null;
    }
    
    
    
